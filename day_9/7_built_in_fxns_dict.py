student = {"name": "jon", "address": "KTM"}
# len()
# It gives length of the object. It is applicable only in sequential types
print(len(student))  # 2

# sorted()
# It gives the dictionary keys in sorted form and in list type
print(sorted(student))  # ["address", "name"]

# str()
print(str(student))  # '{"name": "jon", "address": "KTM"}'


# type()
# It gives type of the object passed as an argument
print(type(student))  # dictionary



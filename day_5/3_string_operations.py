# Concatenation (+)
m1 = "Hello"
m2 = "World"
result = m1 + m2
print(result)  # "HelloWorld"


# Repetition / Broadcast (*)
m1 = "Hello"
result = m1 * 3
print(result)  # "HelloHelloHello"


# Membership Operation ('in' and 'not in')

print("h" in "Hello")  # False
print("e" in "Hello")  # True
print("o" not in "Hello")  # False

